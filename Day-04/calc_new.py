def addition(num1, num2):
    add = num1 + num2
    return add

def subtraction(num1, num2):
    sub = num1 - num2
    return sub

def multiplication(num1, num2):
    mul = num1 + num2
    return mul

def division(num1, num2):
    div = num1 / num2
    return div

print(addition(5, 10))
print(subtraction(5, 15))
print(multiplication(10, 5))
print(division(5, 5))
